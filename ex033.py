#crie um programa que leia 3 numeros e diga qual e o maior e o menor entre eles

n1 = int(input('digite um numero:'))
n2 = int(input('digite outro numero:'))
n3 = int(input('digite mais um numero:'))
if (n1 > n1) and (n1 > n3):
    print(f'{n1} e o maior numero')
elif (n2 > n1) and (n2 > n3):
    print(f'{n2} e o maior numero')
elif (n3 > n1) and (n3 > n2):
    print(f'n3 e o maior numero')
if (n1 < n2) and (n1 < n3):
    print(f'{n1} e o menor numero')
elif (n2 < n1) and (n2 < n3):
    print(f'{n2} e o menor numero')
else:
    print(f'{n3} e o menor numero')
